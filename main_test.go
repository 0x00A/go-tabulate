package tabulate

import "testing"

func TestOutput(t *testing.T) {

	var a1 = []string{"foo", "barrrrrr", "bazz", "blaaaaa"}

	var s1 string = Tabulate(80, a1)
	var s2 string = "foo       barrrrrr  bazz      blaaaaa   \r\n"

	if s1 != s2 {
		t.Error("formatted incorrectly")
	}

	var a2 = []string{"12345678901234567819", "12345678901234567829", "12345678901234567839", "12345678901234567849", "12345678901234567859", "12345678901234567890", "12345678901234567891", "12345678901234567892", "12345678901234567893", "12345678901234567894", "12345678901234567895", "12345678901234567896", "12345678901234567897", "123456789", "12345678901234567899"}

	var s3 string = Tabulate(120, a2)
	var s4 string = "12345678901234567819  12345678901234567829  12345678901234567839  12345678901234567849  12345678901234567859\r\n12345678901234567890  12345678901234567891  12345678901234567892  12345678901234567893  12345678901234567894\r\n12345678901234567895  12345678901234567896  12345678901234567897  123456789             12345678901234567899\r\n"

	if s3 != s4 {
		t.Error("formatted incorrectly")
	}

}
